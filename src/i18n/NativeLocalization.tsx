function translateDynamic(value: string): string | null {
  let match = value.match(/^(\d+) keys?$/);
  if (match) return `${match[1]} 个密钥`;
  match = value.match(/^(\d+) msgs$/);
  if (match) return `${match[1]} 条消息`;
  match = value.match(/^(\d+) tools?$/i);
  if (match) return `${match[1]} 个工具`;
  match = value.match(/^(\d+) loads$/);
  if (match) return `${match[1]} 次加载`;
  match = value.match(/^(\d+) lines$/);
  if (match) return `${match[1]} 行`;
  match = value.match(/^(\d+) SET$/);
  if (match) return `${match[1]} 个已设置`;
  match = value.match(/^(\d+) min ago$/);
  if (match) return `${match[1]} 分钟前`;
  match = value.match(/^Today (.+)$/);
  if (match) return `今天 ${match[1]}`;
  match = value.match(/^Tomorrow (.+)$/);
  if (match) return `明天 ${match[1]}`;
  match = value.match(/^Sun (.+)$/);
  if (match) return `星期日 ${match[1]}`;
  match = value.match(/^Jul (\d+)$/);
  if (match) return `7 月 ${match[1]} 日`;
  match = value.match(/^EXPIRES IN (.+)$/);
  if (match) return `${match[1]} 后过期`;
  match = value.match(/^Active sessions:\s*(\d+)$/);
  if (match) return `活跃会话：${match[1]}`;
  match = value.match(/^Gateway status:\s*(.*)$/);
  if (match) return `网关状态：${ZH_TRANSLATIONS[match[1]!] ?? match[1]}`;
  match = value.match(/^Edit (.+) SOUL\.md$/);
  if (match) return `编辑 ${match[1]} 的 SOUL.md`;
  match = value.match(/^(.+) actions$/);
  if (match) return `${match[1]} 操作`;
  match = value.match(/^(.+) settings$/);
  if (match) return `${match[1]} 设置`;
  match = value.match(/^Share: (.+)$/);
  if (match) return `分享：${match[1]}`;
  match = value.match(/^Message #(.+)$/);
  if (match) return `发送消息到 #${match[1]}`;
  match = value.match(/^Enable (.+)$/);
  if (match) return `启用 ${match[1]}`;
  match = value.match(/^Delete (.+)\?$/);
  if (match) return `删除 ${match[1]}？`;
  match = value.match(/^Clear (.+)$/);
  if (match) return `清除 ${match[1]}`;
  match = value.match(/^Reveal (.+)$/);
  if (match) return `显示 ${match[1]}`;
  match = value.match(/^Hide (.+)$/);
  if (match) return `隐藏 ${match[1]}`;
  return null;
}

